export interface WeatherData {
  location: {
    name: string;
    country: string;
    coordinates: {
      lat: number;
      lon: number;
    };
  };
  current: {
    temperature: number;
    feelsLike: number;
    condition: string;
    description: string;
    humidity: number;
    windSpeed: number;
    visibility: number;
    uvIndex: number;
    icon: string;
  };
  forecast: ForecastItem[];
  timestamp: Date;
}

export interface ForecastItem {
  date: Date;
  temperature: {
    min: number;
    max: number;
  };
  condition: string;
  description: string;
  precipitationChance: number;
  icon: string;
}

